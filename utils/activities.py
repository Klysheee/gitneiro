import random

async def suggest_activity(ctx):
    activities = [
        "Пройдите в парк и насладитесь природой.",
        "Посмотрите новый фильм или сериал.",
        "Прочтите книгу или статью по интересной теме.",
        "Попробуйте приготовить новое блюдо.",
        "Занимайтесь спортом или прогулкой на свежем воздухе."
    ]
    await ctx.send(random.choice(activities))
